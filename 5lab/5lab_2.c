#include <stdio.h>
#include <stdlib.h>
#include <stdbool.h>

int main() {
    int k, value, index = -1; // Объявление переменных k, value и index
    bool found = false; // Объявление переменной found и присваивание ей значения false
    printf("Введите значение k: "); // Вывод сообщения на экран
    scanf("%d", &k); // Ввод значения k с клавиатуры
    int *C = (int *)malloc(k * sizeof(int)); // Выделение памяти для массива C размера k
    printf("Введите %d элементов массива C: ", k); // Вывод сообщения на экран
    for (int i = 0; i < k; i++) { // Цикл для ввода элементов массива C
        scanf("%d", &C[i]); // Ввод i-го элемента массива C с клавиатуры
    }
    printf("Введите значение для удаления: "); // Вывод сообщения на экран
    scanf("%d", &value); // Ввод значения value с клавиатуры
    for (int i = 0; i < k; i++) { // Цикл для поиска значения value в массиве C
        if (C[i] == value) { // Проверка условия, что i-й элемент массива равен value
            if (found) { // Проверка условия, что значение уже было найдено ранее
                index = -1; // Присваивание переменной index значения -1
                break; // Выход из цикла
            } else {
                index = i; // Присваивание переменной index значения i
                found = true; // Присваивание переменной found значения true
            }
        }
    }
    if (index != -1) { // Проверка условия, что значение было найдено и встречается один раз в массиве C
        for (int i = index; i < k - 1; i++) { // Цикл для удаления элемента из массива C путем сдвига оставшихся элементов на одну позицию влево
            C[i] = C[i + 1];
        }
        k--;
        printf("Массив после удаления: ");
        for (int i = 0; i < k; i++) {
            printf("%d ", C[i]);
        }
        printf("\n");
    } else {
        printf("Значение не найдено или встречается более одного раза.\n");
    }
    free(C); // Освобождение памяти, выделенной под массив C
    return 0;
}
